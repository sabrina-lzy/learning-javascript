// For Loop Examples
var count = 0;
for (var i = 0; i < 5; i++) {
 count = count + i;
}


for (var berries = 5; berries > 0; berries--) {
 alert("Eating a berry");
}



// While Loop Examples
var tops = 5;
while (tops > 0) {
 for (var spins = 0; spins < 3; spins++) {
 alert("Top is spinning!");
 }
 tops = tops - 1;
}
